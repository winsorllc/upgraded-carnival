function runTest(name, fn) {
  console.log(`\n▶ ${name}`);
  try {
    fn();
    console.log('✓ PASSED');
    return true;
  } catch (err) {
    console.error('✗ FAILED:', err.message);
    return false;
  }
}

function assertExists(path, msg) {
  if (!fs.existsSync(path)) {
    throw new Error(`${msg}: ${path} does not exist`);
  }
}

function assertEqual(actual, expected, msg) {
  if (actual !== expected) {
    throw new Error(`${msg}: expected ${expected}, got ${actual}`);
  }
}

// Test Suite
let passed = 0;
let failed = 0;

// Test 1: Module exports
if (runTest('Module exports', () => {
  const mod = require('./capture.js');
  assertEqual(typeof mod.captureImage, 'function', 'captureImage should be a function');
  assertEqual(typeof mod.VisionAnalyzer, 'function', 'VisionAnalyzer should be a class');
})) passed++; else failed++;

// Test 2: VisionAnalyzer instantiation
